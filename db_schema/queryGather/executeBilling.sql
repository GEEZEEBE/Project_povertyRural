			, TB_UAST_PRIC PRIC
			, TB_UAST_CODE GODS_DVSN_CD
			, TB_UAST_CODE PRIC_TYPE
		WHERE 1 = 1
		AND GODS.DEL_YN = 'N'
		AND GODS.SRVC_ID = CTGR.SRVC_ID
		AND CTGR.SRVC_ID LIKE '%'	-- terms service catagory, need to change like to = or in	-->
		AND GODS.GODS_NAME LIKE '%'	-- terms Product name	-->
		AND GODS.GODS_ID = PRIC.TAGET_ID
