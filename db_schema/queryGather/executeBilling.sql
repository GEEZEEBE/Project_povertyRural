			, TB_UAST_ASSETS ASSETS
			, TB_UAST_CODE ASSETS_TYPE
			, TB_UAST_GODS GODS
			, TB_UAST_SRVC_CTGR CTGR
			, TB_UAST_PRIC PRIC
			, TB_UAST_CODE PRIC_TYPE
		WHERE 1 = 1
		AND ITEM.DEL_YN = 'N'
		AND ASSETS.ASSET_ID LIKE '%'	-- terms ASSETS TYPE, need to change like to = --> 
